namespace CampaignChain\Hook\AssigneeBundle\Model;

use CampaignChain\CoreBundle\Entity\User;

class Assignee
{
    protected $user;

    /**
     * Set user
     *
     * @param User $user
     * @return Assignee
     */
    public function setUser(User $user = null)
    {
        $this->user = $user;

        return $this;
    }

    /**
     * Get user
     *
     * @return User
     */
    public function getUser()
    {
        return $this->user;
    }
}
